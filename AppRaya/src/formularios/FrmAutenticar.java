    private void BtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarActionPerformed
        // TODO add your handling code here:
        if(autenticarUsuario(this.TfUser.getText(), this.TfPw.getText())){
            FrmMenu menu = new FrmMenu();
            menu.setListaU(listaU);
            menu.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Error al auntenticar...", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_BtnAceptarActionPerformed

    private  boolean autenticarUsuario(String user, String pw){
        boolean b= false;
        for(Usuario U : listaU){
            b = U.verficarUserName(user, pw);
            if(b) return true;
        }
        return b;
    }
    
